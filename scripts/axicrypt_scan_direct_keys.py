RCON = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]


def xtime(a: int) -> int:
    return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1)


def mix_single_column(a: list[int]) -> None:
    t = a[0] ^ a[1] ^ a[2] ^ a[3]
    u = a[0]
    a[0] ^= t ^ xtime(a[0] ^ a[1])
    a[1] ^= t ^ xtime(a[1] ^ a[2])
    a[2] ^= t ^ xtime(a[2] ^ a[3])
    a[3] ^= t ^ xtime(a[3] ^ u)


def expand_key(key: bytes) -> list[int]:
    expanded = list(key)
    bytes_generated = 16
    rcon_iter = 0
    while bytes_generated < 176:
        temp = expanded[-4:]
        if bytes_generated % 16 == 0:
            temp = temp[1:] + temp[:1]
            temp = [SBOX[b] for b in temp]
            temp[0] ^= RCON[rcon_iter]
            rcon_iter += 1
        for b in temp:
            expanded.append(expanded[bytes_generated - 16] ^ b)
            bytes_generated += 1
    return expanded


def encrypt_block(block: bytes, key: bytes) -> bytes:
    state = list(block)
    rk = expand_key(key)
    for i in range(16):
        state[i] ^= rk[i]
    for rnd in range(1, 10):
        state = [SBOX[b] for b in state]
        state = [
            state[0], state[5], state[10], state[15],
            state[4], state[9], state[14], state[3],
            state[8], state[13], state[2], state[7],
            state[12], state[1], state[6], state[11],
        ]
        for col in range(4):
            column = state[col * 4 : col * 4 + 4]
            mix_single_column(column)
            state[col * 4 : col * 4 + 4] = column
        for i in range(16):
            state[i] ^= rk[rnd * 16 + i]
    state = [SBOX[b] for b in state]
    state = [
        state[0], state[5], state[10], state[15],
        state[4], state[9], state[14], state[3],
        state[8], state[13], state[2], state[7],
        state[12], state[1], state[6], state[11],
    ]
    for i in range(16):
        state[i] ^= rk[160 + i]
    return bytes(state)


def bswap_words(candidate: bytes) -> bytes:
    return b"".join(candidate[i : i + 4][::-1] for i in range(0, 16, 4))


def transforms(candidate: bytes) -> list[tuple[str, bytes]]:
    return [
        ("raw", candidate),
        ("rev", candidate[::-1]),
        ("bswap32", bswap_words(candidate)),
        ("rev_words", b"".join(candidate[i : i + 4] for i in range(12, -1, -4))),
    ]


def scan(path: Path, block: bytes, target: bytes, stride: int = 1) -> None:
    data = path.read_bytes()
    hits = []
    for off in range(0, len(data) - 15, stride):
        candidate = data[off : off + 16]
        for name, key in transforms(candidate):
            if encrypt_block(block, key) == target:
                hits.append((off, name, key.hex()))
                print(f"HIT {path.name} off=0x{off:x} transform={name} key={key.hex()}")
    if not hits:
        print(f"NO_HIT {path.name} stride={stride}")


def main() -> int:
    iv = bytes.fromhex("111961f4f0c673d026044552c6d7ac45")
    plain = bytes.fromhex("726a2a66726e5b585776703c3d5b212d")
    cipher = bytes.fromhex("fd8b0a7e65036ded8162c20fbe3c80f2")
    block = bytes(a ^ b for a, b in zip(iv, plain))
    stride = int(os.environ.get("STRIDE", "1"), 0)
    for arg in sys.argv[1:]:
        scan(Path(arg), block, cipher, stride=stride)
    return 0


